package com.armchairfun.poker.common;

/**
 * Constants to store the rank of each possible hand Low to High e.g High Card =
 * lowest Royal Flush = highest
 * 
 * Each Players hand can be assigned a rank, to determine winner(s)
 * 
 * @author cjohn
 * 
 */
public class HandRank {

	public static final int HIGH_CARD = 1;
	public static final int PAIR = 2;
	public static final int TWO_PAIR = 3;
	public static final int THREE_OF_A_KIND = 4;
	public static final int STRAIGHT = 5;
	public static final int FLUSH = 6;
	public static final int FULL_HOUSE = 7;
	public static final int FOUR_OF_A_KIND = 8;
	public static final int STRAIGHT_FLUSH = 9;
	public static final int ROYAL_FLUSH = 10;
	
	

}
